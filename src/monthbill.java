        try{
           
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/electricity" , "root" , "123456");
            
            String meternumber = jTextField1.getText();
            String month = jTextField2.getText();
            
            Statement stm = con.createStatement();
            
            String sql = "select * from bill where meternumber = '" +meternumber+"' and month = '" +month+"'  ";
            
            ResultSet rs = stm.executeQuery(sql);
            
            if(rs.next())
            {
                jLabel8.setText(rs.getString(2));
                jLabel9.setText(rs.getString(3));
                jLabel10.setText(rs.getString(5));
                jLabel11.setText(rs.getString(7));
              
            }
            else
            {
                  JOptionPane.showMessageDialog(this, "Data is not found" , "Error" , JOptionPane.INFORMATION_MESSAGE );
            }
            
            con.close();
                
        }catch(Exception e)
        {
            System.out.println(e.getMessage());
        }
        
